import Control.Concurrent

showMap' _ _ [] = ""
showMap' 0 m map = "\n" ++ showMap' m m map
showMap' i m (x:map)
    | x == 0 = " ◻" ++ map'
    | x == 1 = " ■" ++ map'
    | otherwise = " ？" ++ map'
    where 
        map' = showMap' (i-1) m map

showMap m map = do
    putStrLn $ showMap' m m map 
    putStrLn ""

getValue x y m map
    | x < 0 || y < 0 || x >= m || y >= m = 0
    | otherwise = map !! i
    where
        i = x + y * m

sumOfNeighbers x y m map = c1 + c2 + c3 + c4 + c5 + c6 + c7 + c8
    where
        c1 = getValue (x-1) (y-1) m map
        c2 = getValue  x    (y-1) m map
        c3 = getValue (x+1) (y-1) m map
        c4 = getValue (x-1)  y    m map
        c5 = getValue (x+1)  y    m map
        c6 = getValue (x-1) (y+1) m map
        c7 = getValue  x    (y+1) m map
        c8 = getValue (x+1) (y+1) m map

conway' 0 _ _ = []
conway' i m map = (conway' (i-1) m map) ++ [c2]
    where
        c = map !! (i-1)
        x = (i-1) `mod` m
        y = (i-1) `div` m
        sum = sumOfNeighbers x y m map
        c2 = case c of 1 -> (case sum of
                                0 -> 0
                                1 -> 0
                                2 -> 1
                                3 -> 1
                                otherwise -> 0)
                       0 -> (case sum of
                                3 -> 1
                                otherwise -> 0)

conway map = do
    let l = length map
    let m = round $ sqrt $ fromIntegral l
    showMap m map
    threadDelay 500000
    conway $ conway' l m map

